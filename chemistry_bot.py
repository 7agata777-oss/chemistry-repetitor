@dp.message(Quiz.task_answer)
async def check_task_answer(message: types.Message, state: FSMContext):
    data = await state.get_data()
    topic_id = data["current_topic"]
    idx = data["task_index"]
    correct = data["task_correct"]
    tasks = TOPICS[topic_id]["tasks"]
    prob = tasks["problems"][idx]
    user_answer = message.text.strip()
    expected = prob["answer"].strip()
    if user_answer.lower() == expected.lower():
        correct += 1
        await message.answer("✅ Верно!")
    else:
        await message.answer(f"❌ Неверно. Правильный ответ: {expected}\n\n{prob.get('explanation','')}")
    idx += 1
    await state.update_data(task_index=idx, task_correct=correct)
    if idx < len(tasks["problems"]):
        await message.answer(f"Задача {idx+1}:\n{tasks['problems'][idx]['question']}")
    else:
        total = len(tasks["problems"])
        await message.answer(f"🎉 Вы решили все задачи по теме «{TOPICS[topic_id]['title']}»!\n"
                             f"Правильных ответов: {correct} из {total}")
        await message.answer("Выберите действие:", reply_markup=main_menu_kb())
        await state.set_state(Quiz.main_menu)

@dp.callback_query(Quiz.question_index, F.data.startswith("ans_"))
async def process_answer(callback: types.CallbackQuery, state: FSMContext):
    data = await state.get_data()
    topic_id = data["current_topic"]
    idx = data["question_index"]
    correct = data["correct_count"]
    choice = int(callback.data.split("_")[1])
    correct_idx = TOPICS[topic_id]["questions"][idx]["correct"]
    is_correct = (choice == correct_idx)
    if is_correct:
        correct += 1
    opts = TOPICS[topic_id]["questions"][idx]["options"]
    correct_ans = opts[correct_idx]
    feedback = "✅ Верно!" if is_correct else f"❌ Неверно. Правильный ответ: {correct_ans}"
    result_text = f"Вопрос {idx+1}:\n{TOPICS[topic_id]['questions'][idx]['q']}\n\nВаш ответ: {opts[choice]}\n{feedback}"
    await callback.message.delete()
    await callback.message.answer(result_text, parse_mode="Markdown")
    idx += 1
    if idx < len(TOPICS[topic_id]["questions"]):
        await state.update_data(question_index=idx, correct_count=correct)
        next_q = TOPICS[topic_id]["questions"][idx]
        await callback.message.answer(
            f"📝 Тест по теме «{TOPICS[topic_id]['title']}»\n\nВопрос {idx+1}:\n{next_q['q']}",
            parse_mode="Markdown", reply_markup=question_kb(topic_id, idx)
        )
    else:
        total = len(TOPICS[topic_id]["questions"])
        percent = (correct / total) * 100
        uid = callback.from_user.id
        prev = user_progress.get(uid, {}).get(topic_id, {"attempts": 0, "best_percent": 0.0})
        new_att = prev["attempts"] + 1
        new_best = max(prev["best_percent"], percent)
        user_progress.setdefault(uid, {})[topic_id] = {"attempts": new_att, "best_percent": new_best}
        save_progress(uid, topic_id, new_att, new_best)
        await callback.message.answer(
            f"🎉 Тема пройдена!\n\nПравильных ответов: {correct} из {total} ({percent:.0f}%)",
            parse_mode="Markdown", reply_markup=after_action_kb(topic_id)
        )
        await state.set_state(Quiz.main_menu)
    await callback.answer()

